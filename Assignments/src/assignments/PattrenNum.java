package assignments;

public class PattrenNum {
	
	public static void main(String args[])   
	{   
	int i, j,number, n=5;   
	//loop for rows  
	for(i=0; i<n; i++)  
	{   
	number=5;   
	//loop for columns  
	for(j=0; j<=i; j++)  
	{   
	//prints num  
	System.out.print(number+ " ");   
	   
	number--;   
	}   
	
	System.out.println();   
	}   
	}   

}
